
public class Bitwise {
	
	public static void main(String[] args) {
		
		System.out.println(2 & 3);
		System.out.println(2 | 3);
		System.out.println(~2);
		System.out.println(2 ^ 3);
		System.out.println(21 << 1);
		System.out.println(24 >> 1);
		System.out.println(-4 << 1);
		System.out.println(-4 >> 1);
		System.out.println(12 <<  3);
	}

}
